                        <div class="entry-summary">
                            <p>মাদারীপুরে শিক্ষক হত্যা চেষ্টার সাথে ছাত্রশিবিরকে জড়িয়ে প্রধানমন্ত্রীর অসত্য বক্তব্যের তীব্র নিন্দা ও প্রতিবাদ জানিয়েছে বাংলাদেশ ইসলামী ছাত্রশিবির। </p>
                            <p>এক যৌথ প্রতিবাদ বার্তায় ছাত্রশিবিরের কেন্দ্রীয় সভাপতি আতিকুর রহমান ও সেক্রেটারী জেনারেল ইয়াছিন আরাফাত বলেন, গুপ্তহত্যা নিয়ে সরকারের ব্যর্থতায় দেশ-বিদেশে যখন জাতির ভাবমর্যাদা ক্ষুন্ন হচ্ছে তখন এ স্পর্শকাতর ঘটনা নিয়ে স্বয়ং প্রধানমন্ত্রী কর্তৃক ছাত্রশিবিরকে জড়িয়ে এ মিথ্যাচারে জনগণ বিক্ষুদ্ধ ও হতবাক। তিনি গতকাল রাজধানীর জাতীয় প্রেসক্লাবে বাংলাদেশ ফেডারেল সাংবাদিক ইউনিয়ন ও ঢাকা সাংবাদিক ইউনিয়নের এক ইফতার মাহফিলে বলেছেন, ”মাদারীপুরে শিক্ষক হত্যাকান্ডের সাথে জড়িত সন্দেহে গ্রেপ্তারকৃত ব্যক্তি ছাত্রশিবির কর্মী”। যা একেবারেই ডাহা মিথ্যাচার। গ্রেপ্তারকৃত ফাহিমকে মাদারীপুর পুলিশ সুপার সারওয়ার হোসেন হিজবুত তাহরীর কর্মী বলে উল্লেখ করেছেন। যা সকল গণমাধ্যমে প্রকাশ হয়েছে। পুলিশ শত চেষ্টার পরেও তার সাথে শিবিরের দূরতম কোন সম্পর্ক খোঁজে পায়নি। বরং তাকে বন্দুকযুদ্ধের নাটক সাজিয়ে হত্যা করে গুরুত্বপূর্ণ তথ্য বিনষ্ট করে দেয়ার কারণে গুপ্ত হত্যায় সরকারের সংশ্লিষ্টতাকেই জনগণ সন্দেহের দৃষ্টিতে দেখছে।</p>

                            <p>আমরা মনে করি, প্রধানমন্ত্রী তার রাষ্ট্রীয় অবস্থান থেকে নয় বরং দলীয় অবস্থান থেকে এই অসত্য বক্তব্য দিয়েছেন। আমরা মনে করিয়ে দিতে চাই, বাংলাদেশে জঙ্গিপনা শুরু হয়েছিল আ.লীগের আমলে এবং আ.লীগের শীর্ষ নেতাদের নিকটাত্বীয় ছিল শীর্ষ জঙ্গী নেতারা। এমনকি সম্প্রতি সরকারের তথাকথিত সাঁড়াশি অভিযানের সময়ও গত ১২ জুন ৬৩ জেলায় একযোগে বোমাহামলার আসামী নড়াইল সদর উপজেলা ছাত্রলীগের সভাপতি গাজী আব্দুল্লাহ আল মামুনকে গ্রেপ্তার করে পুলিশ। তার জঙ্গি সংশ্লিষ্টতার ব্যপারে নিশ্চিত করেন, নড়াইল সদর থানার ওসি সুভাস বিশ্বাস। সুতরাং প্রধানমন্ত্রীর এই বক্তব্য শাক দিয়ে মাছ ঢাকার অপচেষ্টা ছাড়া কিছু নয়। প্রধানমন্ত্রীর এই অসত্য বক্তব্য প্রকৃত অপরাধীদের আড়াল ও সত্য লুকিয়ে রাখতে সহায়ক ভূমিকা পালন করবে। যা জাতি প্রত্যাশা করে না।
                            <p>

                            <p>নেতৃবৃন্দ বলেন, ছাত্রশিবির বরাবরই গুপ্তহত্যাসহ প্রতিটি হত্যাকান্ডের নিন্দা জানিয়ে আসল খুনিদের গ্রেপ্তারের মাধ্যমে দৃষ্টান্তমূলক শাস্তির দাবী জানিয়ে আসছে। কাজেই গুপ্তহত্যার সাথে ছাত্রশিবিরের সম্পৃক্ততার কোন প্রশ্নই আসে না। প্রধানমন্ত্রীর এই বক্তব্যে সত্যের লেশ মাত্র নেই এবং জনগণ এই অসত্য বক্তব্যকে বিশ্বাস করে না। আমরা আশাকরি প্রধানমন্ত্রী দেশ ও জাতির স্বার্থে তার এই অসত্য বক্তব্য প্রত্যাহার করবেন এবং ভবিষ্যতে তার অবস্থান ও দায়িত্বের প্রতি খেয়াল রেখে কথা বলবেন।
