                        <h2 class="entry-title">
                            <a href="blog-detail.html">প্রধানমন্ত্রী অসত্য বক্তব্য দিয়ে জাতিকে বিভ্রান্ত করছেন</a>
                        </h2>
                        <div class="entry-meta">
                            <ul>
                                <li class="publish-date"><i class="fa fa-calendar"></i><a href="#">রবিবার, ১৯ জুন ২০১৬</a></li>
